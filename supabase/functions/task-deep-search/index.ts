        if (followupError) {
          console.error('‚ùå Erreur r√©cup√©ration historique suivi:', followupError);
        }

        console.log('‚úÖ Historique r√©cup√©r√©:', followupHistory?.length || 0, 'questions pr√©c√©dentes');

